
//Integer22.С начала суток прошло N секунд(N — целое).Найти количество се -
//кунд, прошедших с начала последнего часа.

#include "Integer_main.h"

void int2()
{
	int n = 0;

	cout << "Ведiть n: " << endl;
	cin >> n;

	if (0 > n or n > 86400)
	{
		cout << "Ви ввели не коректне значення!" << endl;

		return;
	}

	while (n > 1440)
	{
		n -= 1440;
	}

	cout << "Секунд пройшло з останього часу: " << n << endl;
}